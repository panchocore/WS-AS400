package com.cid.wsAS400;

/**
 *
 * @author fgallardo
 */
public class Producto {
    private String codProducto;
    private String saldo;
    private String descProducto;
    private String presentacion;
    private String factorUnidad;
    private String formaFarmaceutica;
    private String loteStd;
    private String codUnificacion;
    private String area;
    private String factorGranel;
    private String lop;

    public String getCodProducto() {
        return codProducto;
    }

    public void setCodProducto(String codProducto) {
        this.codProducto = codProducto;
    }

    public String getSaldo() {
        return saldo;
    }

    public void setSaldo(String saldo) {
        this.saldo = saldo;
    }

    public String getDescProducto() {
        return descProducto;
    }

    public void setDescProducto(String descProducto) {
        this.descProducto = descProducto;
    }

    public String getPresentacion() {
        return presentacion;
    }

    public void setPresentacion(String presentacion) {
        this.presentacion = presentacion;
    }

    public String getFactorUnidad() {
        return factorUnidad;
    }

    public void setFactorUnidad(String factorUnidad) {
        this.factorUnidad = factorUnidad;
    }

    public String getFormaFarmaceutica() {
        return formaFarmaceutica;
    }

    public void setFormaFarmaceutica(String formaFarmaceutica) {
        this.formaFarmaceutica = formaFarmaceutica;
    }

    public String getLoteStd() {
        return loteStd;
    }

    public void setLoteStd(String loteStd) {
        this.loteStd = loteStd;
    }

    public String getCodUnificacion() {
        return codUnificacion;
    }

    public void setCodUnificacion(String codUnificacion) {
        this.codUnificacion = codUnificacion;
    }

    public String getArea() {
        return area;
    }

    public void setArea(String area) {
        this.area = area;
    }

    public String getFactorGranel() {
        return factorGranel;
    }

    public void setFactorGranel(String factorGranel) {
        this.factorGranel = factorGranel;
    }

    public String getLop() {
        return lop;
    }

    public void setLop(String lop) {
        this.lop = lop;
    }
    
    
}
